<p>Dear American,</p>
<br/>
<p>I have found that running for public office is not just about great innovative ideas and solutions that help our citizens and our country. It also has a dark and uncomfortable underbelly of needing to ask friends, family, and even strangers for money.</p>
<br/>
<p>Apparently, my campaign must have money before people will give it money, and they won’t if it doesn’t. I keep getting advice and pushback that I can’t get people to give money in support of my campaign until I already have people contributing to my campaign. We’ve gotten the message that even the DCCC, whose job it is to financially support Democratic Congressional campaigns, won’t touch an Idaho race because they feel it’s a waste of money!  They have even said that they probably wouldn’t support a congressional candidate that won the last race, because odds show it was an anomaly, and still not worth the expense. This is the current “conventional wisdom” of the Democratic Party.</p>
<br/>
<p>On a personal level, I have even approached Democrats currently in office who have heard my positions and what I have to say. They’ve promised to support me 100%, but only after I show that I have and can raise campaign funds. So much for the traditional American belief that any qualified person could run for office!</p>
<br/>
<p>Folks, in so many ways, this terrible “conventional wisdom” has gotten us where we are today:</p>
<p>
  <ul>
    <li>The Republicans are well funded and we aren’t!</li>
    <li>The Republicans are in office, and we aren’t!</li>
    <li>The wealthy are the only ones able to run for office!</li>
  </ul>
</p>
<br/>
<p>I am not a conventional candidate. I am not willing to follow “conventional wisdom” and do it as it’s always been done.  I firmly believe that America is ready for representatives that are one of them, that understand and have lived in poverty and had to work hard on their way to a better life.  I’m running now because I can’t stand to see anyone try to take that away from us.<p>
<br/>
<p>I need your help to ensure that we ALL have and can keep our voice!</p>
<br/>
<p>Please contribute what you can.</p>
